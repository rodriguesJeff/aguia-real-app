String getUnityLogo(String unityName) {
  switch (unityName) {
    case 'Unidade Águia':
      return 'assets/images/unityes/unidade-aguia.jpeg';
    case 'Águia Marinha':
      return 'assets/images/unityes/unidade-aguia-marinha.jpeg';
    case 'Unidade Gavião':
      return 'assets/images/unityes/unidade-gaviao.jpeg';
    case 'Gaivota Real':
      return 'assets/images/unityes/unidade-gaivota-real.jpeg';
    default:
      return 'assets/images/unityes/unidade-aguia.jpeg';
  }
}
